package officet.officet_vo;

import java.util.Date;

// 결재
public class Approval {

   // 필드
   private int approval_id; // 결재 id
   private int worker_id; // 사원 id
   private int approver_id; // 승인자 id
   private int approval_step; // 결재 단계
   private String step_name; // 승인단계 명
   private int approval_status; // 결재 상태
   private Date approval_request_time; // 결재 요청 시간
   private String approver_name; // 승인자 명
   private String worker_name; // 결재자 명
   private String team_name; // 부서 명
   private String approval_type; // 결재 상태 명
   private String approval_msg; // 결재 메세지 

   // 생성자
   public Approval() {
   }

   public Approval(int approval_id, int worker_id, int approver_id, int approval_step, int approval_status,
         Date approval_request_time, String approver_name, String worker_name, String team_name,
         String approval_type, String approval_msg, String step_name) {
      this.approval_id = approval_id;
      this.worker_id = worker_id;
      this.approver_id = approver_id;
      this.approval_step = approval_step;
      this.approval_status = approval_status;
      this.approval_request_time = approval_request_time;
      this.approver_name = approver_name;
      this.worker_name = worker_name;
      this.team_name = team_name;
      this.step_name = step_name;
   }

   // getter / setter
   public int getApproval_id() {
      return approval_id;
   }

   public void setApproval_id(int approval_id) {
      this.approval_id = approval_id;
   }

   public int getWorker_id() {
      return worker_id;
   }

   public void setWorker_id(int worker_id) {
      this.worker_id = worker_id;
   }

   public int getApprover_id() {
      return approver_id;
   }

   public void setApprover_id(int approver_id) {
      this.approver_id = approver_id;
   }

   public int getApproval_step() {
      return approval_step;
   }

   public void setApproval_step(int approval_step) {
      this.approval_step = approval_step;
   }

   public int getApproval_status() {
      return approval_status;
   }

   public void setApproval_status(int approval_status) {
      this.approval_status = approval_status;
   }

   public Date getApproval_request_time() {
      return approval_request_time;
   }

   public void setApproval_request_time(Date approval_request_time) {
      this.approval_request_time = approval_request_time;
   }

   public String getApprover_name() {
      return approver_name;
   }

   public void setApprover_name(String approval_name) {
      this.approver_name = approval_name;
   }

   public String getWorker_name() {
      return worker_name;
   }

   public void setWorker_name(String worker_name) {
      this.worker_name = worker_name;
   }

   public String getTeam_name() {
      return team_name;
   }

   public void setTeam_name(String team_name) {
      this.team_name = team_name;
   }

   public String getApproval_type() {
      return approval_type;
   }

   public void setApproval_type(String approval_type) {
      this.approval_type = approval_type;
   }

   public String getApproval_msg() {
      return approval_msg;
   }

   public void setApproval_msg(String approval_msg) {
      this.approval_msg = approval_msg;
   }

   public String getStep_name() {
      return step_name;
   }

   public void setStep_name(String step_name) {
      this.step_name = step_name;
   }

}